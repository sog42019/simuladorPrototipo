    private void BotonAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAñadirActionPerformed
        // TODO add your handling code here:
        if (TCp.getText().equals("")||(TCp.getText().equals("0"))){
            JOptionPane.showMessageDialog(rootPane, "Debe definir la cantidad de procesos");
        } else {
            contProcesos = Integer.parseInt(TCp.getText());
            TCp.setEnabled(false);
            }
        
        
    }//GEN-LAST:event_BotonAñadirActionPerformed

    private void jButtonAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAñadirActionPerformed
        // TODO add your handling code here:
        if (contProcesos > 0) {
            contProcesos = contProcesos - 1;
            if(TamProc1.getText().equals("")||(TA.getText().equals(""))||(CPU1.getText().equals(""))||(CPU2.getText().equals(""))||(CPU3.getText().equals(""))||(RE.getText().equals(""))||(RS.getText().equals(""))){
                JOptionPane.showMessageDialog(rootPane, "No completo todos los espacios");
            }
            if(TamProc1.getText().equals("0")||(CPU1.getText().equals("0"))||(CPU2.getText().equals("0"))||(CPU3.getText().equals("0"))||(RE.getText().equals("0"))||(RS.getText().equals("0"))){
                JOptionPane.showMessageDialog(rootPane, "Unicamente el Tiempo de Arribo puede ser 0");
            }
            contID = contID + 1;
            nuevoProceso = new Proceso(contID,Integer.parseInt(TA.getText()), Integer.parseInt(TamProc1.getText()), Integer.parseInt(CPU1.getText()), Integer.parseInt(CPU2.getText()), Integer.parseInt(CPU3.getText()), Integer.parseInt(RE.getText()), Integer.parseInt(RS.getText()));
            listaProcesos.add(nuevoProceso);
            GenerarTabla();
        } else { JOptionPane.showMessageDialog(rootPane, "No se pueden añadir mas procesos");
        }
        
    }//GEN-LAST:event_jButtonAñadirActionPerformed

