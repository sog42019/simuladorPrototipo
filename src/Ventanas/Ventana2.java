    public void GenerarTabla() {     
        String data [][] = new String [listaProcesos.size()][8];
        for (int i = 0; i < listaProcesos.size(); i++) {
                data[i][0] = Integer.toString(listaProcesos.get(i).getID());
                data[i][1] = Integer.toString(listaProcesos.get(i).getTamaño());
                data[i][2] = Integer.toString(listaProcesos.get(i).getTA());
                data[i][3] = Integer.toString(listaProcesos.get(i).getCPU1());
                data[i][4] = Integer.toString(listaProcesos.get(i).getEntrada());
                data[i][5] = Integer.toString(listaProcesos.get(i).getCPU2());
                data[i][6] = Integer.toString(listaProcesos.get(i).getSalida());
                data[i][7] = Integer.toString(listaProcesos.get(i).getCPU3());
        }
        jTableV2.setModel(new javax.swing.table.DefaultTableModel(
            data, new String [] {"ID", "Tamaño", "T Arribo", "CPU 1", "Entrada", "CPU 2","Salida", "CPU 3"}
        ));
    }
    
    
    
